package Switch;

public class SwitchProgram {
    public static void main(String[] args) {

        int num = 3;
        switch (num) {
        case 1:
            System.out.println("one");
            break;
        case 2:
            System.out.println("two");
            break;
        case 3:
            System.out.println("three");
            break;
        case 4:
            System.out.println("four");
            break;
        }
    }
}
